/*******************************************************************************************************************//**
 * @brief Open and configures the OV5645. Implements @ref rm_ovxxxx_api_t::open.
 *
 * @retval FSP_SUCCESS                  OV5645 successfully open and configure.
 * @retval FSP_ERR_ASSERTION            Null pointer, or one or more configuration options is invalid.
 * @retval FSP_ERR_ALREADY_OPEN         Module is already open.  This module can only be opened once.
 **********************************************************************************************************************/
fsp_err_t RM_OV5645_Open (rm_ovxxxx_ctrl_t * const p_api_ctrl, rm_ovxxxx_cfg_t const * const p_cfg)
{
    fsp_err_t err = FSP_SUCCESS;
    rm_ov5645_instance_ctrl_t * p_ctrl = (rm_ov5645_instance_ctrl_t *) p_api_ctrl;

#if RM_OV5645_CFG_PARAM_CHECKING_ENABLE
    FSP_ASSERT (NULL != p_ctrl);
    FSP_ASSERT (NULL != p_cfg);
    FSP_ASSERT (NULL != p_cfg->p_instance);
    FSP_ERROR_RETURN (RM_OV5645_OPEN != p_ctrl->open, FSP_ERR_ALREADY_OPEN);
#endif

    p_ctrl->p_cfg                = p_cfg;
    p_ctrl->open                 = RM_OV5645_OPEN;
    p_ctrl->p_context            = p_cfg->p_context;
    p_ctrl->p_i2c_instance       = p_cfg->p_i2c_instance;

    err = p_ctrl->p_i2c_instance->p_api->open(p_ctrl->p_i2c_instance->p_ctrl, p_ctrl->p_i2c_instance->p_cfg);
    FSP_ERROR_RETURN (FSP_SUCCESS == err, err);

    /* Power on */
    err = ov5645_s_power(p_ctrl, 1);

    return FSP_SUCCESS;
}

/*******************************************************************************************************************//**
 * @brief Close OV5645. Implements @ref rm_ovxxxx_api_t::close.
 *
 * @retval FSP_SUCCESS              Successfully closed.
 * @retval FSP_ERR_ASSERTION        Null pointer passed as a parameter.
 * @retval FSP_ERR_NOT_OPEN         Module is not open.
 **********************************************************************************************************************/
fsp_err_t RM_OV5645_Close (rm_ovxxxx_ctrl_t * const p_api_ctrl)
{
    rm_ov5645_instance_ctrl_t * p_ctrl = (rm_ov5645_instance_ctrl_t *) p_api_ctrl;

#if RM_OV5645_CFG_PARAM_CHECKING_ENABLE
    FSP_ASSERT(NULL != p_ctrl);
    FSP_ERROR_RETURN(RM_OV5645_OPEN == p_ctrl->open, FSP_ERR_NOT_OPEN);
#endif

    ov5645_s_power(p_ctrl, 0);
    p_ctrl->p_i2c_instance->p_api->close(p_ctrl->p_i2c_instance->p_ctrl);

    /* Clear Open flag */
    p_ctrl->open = 0;

    return FSP_SUCCESS;
}


