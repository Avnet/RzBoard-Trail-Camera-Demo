fsp_err_t ov5645_write_reg (rm_ov5645_instance_ctrl_t *p_ctrl, uint16_t reg, uint8_t val)
{
    uint8_t regbuf[3];
    fsp_err_t err = FSP_SUCCESS;
    uint32_t  timeout_ms = I2C_TRANSACTION_BUSY_DELAY;

    regbuf[0] = (uint8_t) (reg >> 8);
    regbuf[1] = (uint8_t) (reg & 0xff);
    regbuf[2] = val;

    g_i2c_callback_event = I2C_MASTER_EVENT_ABORTED;
    err = p_ctrl->p_i2c_instance->p_api->write(p_ctrl->p_i2c_instance->p_ctrl, &regbuf[0], 3, false);
    while ((I2C_MASTER_EVENT_TX_COMPLETE != g_i2c_callback_event) && timeout_ms)
    {
        timeout_ms--;
    }
    FSP_ERROR_RETURN(timeout_ms != 0, FSP_ERR_TIMEOUT);
    return err;
}

fsp_err_t ov5645_read_reg(rm_ov5645_instance_ctrl_t *p_ctrl, uint16_t reg, uint8_t *val)
{
    uint8_t regbuf[2];
    fsp_err_t err = FSP_SUCCESS;
    g_i2c_callback_event = I2C_MASTER_EVENT_ABORTED;
    uint32_t  timeout_ms = I2C_TRANSACTION_BUSY_DELAY;

    regbuf[0] = (uint8_t) (reg >> 8);
    regbuf[1] = (uint8_t) (reg & 0xff);

    err = p_ctrl->p_i2c_instance->p_api->write(p_ctrl->p_i2c_instance->p_ctrl, &regbuf[0], 2, false);
    while ((I2C_MASTER_EVENT_TX_COMPLETE != g_i2c_callback_event) && timeout_ms)
    {
        timeout_ms--;
    }

    timeout_ms = I2C_TRANSACTION_BUSY_DELAY;
    err = p_ctrl->p_i2c_instance->p_api->read(p_ctrl->p_i2c_instance->p_ctrl, val, 1, false);
    while ((I2C_MASTER_EVENT_RX_COMPLETE != g_i2c_callback_event) && timeout_ms)
    {
        timeout_ms--;
    }
    FSP_ERROR_RETURN(timeout_ms != 0, FSP_ERR_TIMEOUT);

    return err;
}

fsp_err_t ov5645_set_register_array(rm_ov5645_instance_ctrl_t *p_ctrl, const struct reg_value *settings, uint32_t num_settings)
{
    uint32_t i;
    fsp_err_t err = FSP_SUCCESS;

    for (i = 0; i < num_settings; ++i, ++settings)
    {
        err = ov5645_write_reg(p_ctrl, settings->reg, settings->val);
        FSP_ERROR_RETURN(FSP_SUCCESS == err, err);
    }
    return err;
}



fsp_err_t ov5645_s_power(rm_ov5645_instance_ctrl_t *p_ctrl, uint32_t on)
{
    fsp_err_t err = FSP_SUCCESS;

    if (1 == on)
    {
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_02_PIN_00, BSP_IO_LEVEL_LOW);
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_40_PIN_02, BSP_IO_LEVEL_LOW);

        /* 1 msec wait */
        //R_BSP_SoftwareDelay(1, BSP_DELAY_UNITS_MILLISECONDS);
        vTaskDelay(1);

        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_02_PIN_00, BSP_IO_LEVEL_HIGH);
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_40_PIN_02, BSP_IO_LEVEL_LOW);

        /* 20 msec wait */
        //R_BSP_SoftwareDelay(20, BSP_DELAY_UNITS_MILLISECONDS);
        vTaskDelay(1);

        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_02_PIN_00, BSP_IO_LEVEL_HIGH);
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_40_PIN_02, BSP_IO_LEVEL_HIGH);

        /* 20 msec wait */
        //R_BSP_SoftwareDelay(20, BSP_DELAY_UNITS_MILLISECONDS);
        vTaskDelay(20);

        /* Reset ov564x sensor */
        err = ov5645_set_register_array(p_ctrl, ov564x_reset,
                                        sizeof(ov564x_reset) / sizeof(struct reg_value));

        /* Delay for reset ov564x sensor */
        //R_BSP_SoftwareDelay(10, BSP_DELAY_UNITS_MICROSECONDS);
        vTaskDelay(1);

        /* Write global config to ov5640 sensor */
        err = ov5645_set_register_array(p_ctrl, ov5640_global_init_setting,
                                        sizeof(ov5640_global_init_setting) / sizeof(struct reg_value));

        FSP_ERROR_RETURN(FSP_SUCCESS == err, err);

        /* usleep_range(500, 1000); */
        //R_BSP_SoftwareDelay(1000U, BSP_DELAY_UNITS_MICROSECONDS);

    }
    else
    {
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_02_PIN_00, BSP_IO_LEVEL_HIGH);
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_40_PIN_02, BSP_IO_LEVEL_HIGH);

        /* 1 msec wait */
        //R_BSP_SoftwareDelay(1, BSP_DELAY_UNITS_MILLISECONDS);
        vTaskDelay(1);

        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_02_PIN_00, BSP_IO_LEVEL_HIGH);
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_40_PIN_02, BSP_IO_LEVEL_LOW);

        /* 20 msec wait */
        //R_BSP_SoftwareDelay(20, BSP_DELAY_UNITS_MILLISECONDS);
        vTaskDelay(20);

        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_02_PIN_00, BSP_IO_LEVEL_LOW);
        R_BSP_PinWrite((bsp_io_port_pin_t) BSP_IO_PORT_40_PIN_02, BSP_IO_LEVEL_LOW);

        /* 20 msec wait */
        //R_BSP_SoftwareDelay(20, BSP_DELAY_UNITS_MILLISECONDS);
        vTaskDelay(20);
    }

    return FSP_SUCCESS;
}

/*******************************************************************************************************************//**
 * @brief Enable or disable streaming data in OV5645. Implements @ref rm_ovxxxx_api_t::stream.
 *
 * enable:
 *      1: Enable streaming data in OV5645
 *      Others: Disable streaming data in OV5645
 *
 * @retval FSP_SUCCESS              Successfully closed.
 * @retval FSP_ERR_ASSERTION        Null pointer passed as a parameter.
 * @retval FSP_ERR_NOT_OPEN         Module is not open.
 **********************************************************************************************************************/
fsp_err_t RM_OV5645_STREAM(rm_ovxxxx_ctrl_t * const p_api_ctrl, uint32_t enable)
{
    rm_ov5645_instance_ctrl_t * p_ctrl = (rm_ov5645_instance_ctrl_t *) p_api_ctrl;
    fsp_err_t err = FSP_SUCCESS;

#if RM_OV5645_CFG_PARAM_CHECKING_ENABLE
    FSP_ASSERT(NULL != p_ctrl);
    FSP_ERROR_RETURN(RM_OV5645_OPEN == p_ctrl->open, FSP_ERR_NOT_OPEN);
#endif

    if (1 == enable)
    {
#if RM_OV5645_CFG_CAPTURE_SIZE_1280_720_HD
        err = ov5645_set_register_array(ov5645_setting_720P_1280_720,
                                            sizeof(ov5645_setting_720P_1280_720) / sizeof(struct reg_value));
#elif RM_OV5645_CFG_CAPTURE_SIZE_1920_1080_FHD
        /* Write global setting_1080P_1920_1080 config to ov5640 sensor */
        err = ov5645_set_register_array(p_ctrl, ov5640_setting_1080P_1920_1080,
                                                   sizeof(ov5640_setting_1080P_1920_1080) / sizeof(struct reg_value));

#else
        err = FSP_ERR_UNSUPPORTED;
#endif
        FSP_ERROR_RETURN(FSP_SUCCESS == err, err);
    }
    else
    {
        /* Do nothing */
    }

    return FSP_SUCCESS;
}



